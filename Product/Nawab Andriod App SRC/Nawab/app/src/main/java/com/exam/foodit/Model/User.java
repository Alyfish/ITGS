package com.exam.foodit.Model;

public class User {
    String userId, fnaem, lastnme, image, email, passwrod;

    public User(){

    }

    public String getUserId() {
        return userId;
    }

    public void setUserId(String userId) {
        this.userId = userId;
    }

    public String getFnaem() {
        return fnaem;
    }

    public void setFnaem(String fnaem) {
        this.fnaem = fnaem;
    }

    public String getLastnme() {
        return lastnme;
    }

    public void setLastnme(String lastnme) {
        this.lastnme = lastnme;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getPasswrod() {
        return passwrod;
    }

    public void setPasswrod(String passwrod) {
        this.passwrod = passwrod;
    }
}
